#include <stdio.h>

int main() {
    float x = 0.478984;          // Initialize float x
    float y = 5489451.245847;    // Initialize float y
    float f = 1654.0000;         // Initialize float f
    float z = 0.00000;           // Initialize float z

    z = -565.21547;              // Assign a new value to z

    // Print the values of the float variables
    printf("Value of x: %f\n", x);
    printf("Value of y: %f\n", y);
    printf("Value of f: %f\n", f);
    printf("Value of z after assignment: %f\n", z);

    return 0;
}
