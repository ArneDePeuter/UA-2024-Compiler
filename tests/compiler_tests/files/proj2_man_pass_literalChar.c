#include <stdio.h>

int main() {
    char x = 'x';    // Initialize char x with 'x'
    char dot = '.';  // Initialize char dot with '.'

    // Print the characters and their ASCII values
    printf("Character x: %c, ASCII value: %d\n", x, x);
    printf("Character dot: %c, ASCII value: %d\n", dot, dot);

    return 0;
}
